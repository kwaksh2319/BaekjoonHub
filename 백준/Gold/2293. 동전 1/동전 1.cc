int main() {
	ios::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);
	int n,k;
	vector<int>coins;
	cin >> n >> k;
	for (int i = 0; i < n; i++) {
		int tmp;
		cin >> tmp;
		coins.push_back(tmp);
	}
	vector<int>dp;
	for (int i = 0; i <= k; i++) {
		dp.push_back(0);
	}
	dp[0] = 1;
	for (int i = 0; i < coins.size(); i++) {
		for (int j = coins[i]; j <= k; j++) {
			dp[j] = dp[j] + dp[j - coins[i]];
		}
	}
	cout << dp[k];
	return 0;
}